export const getClassName = (piece: Piece | 'ghost'): string => {
  switch (piece) {
    case 'I':
      return 'piece-I';
    case 'J':
      return 'piece-J';
    case 'L':
      return 'piece-L';
    case 'O':
      return 'piece-O';
    case 'S':
      return 'piece-S';
    case 'T':
      return 'piece-T';
    case 'Z':
      return 'piece-Z';
    case 'ghost':
      return 'piece-preview';
    case 'i':
      return 'piece-i';
    case 'ii':
      return 'piece-ii';
    case 'll':
      return 'piece-ll';

    default: {
      const exhaustiveCheck: never = piece;
      throw new Error(`Unhandled piece case: ${exhaustiveCheck}`);
    }
  }
};
